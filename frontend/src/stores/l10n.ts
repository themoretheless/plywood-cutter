export function useL10n() {
  const t = (key: string) => {
    if (lang.value === 'en' && en[key]) return en[key]
    return ru[key] ?? key
  }
  const toggleLang = () => { lang.value = lang.value === 'ru' ? 'en' : 'ru' }
  return { lang, t, toggleLang }
}
